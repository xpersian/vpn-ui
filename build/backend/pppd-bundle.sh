#!/bin/sh
#
# build/backend/pppd-bundle.sh — assemble the relocatable pppd bundle.
#
# Runs INSIDE an Alpine (musl) container. pppd can't be a single static binary
# because it dlopens plugins (radius.so for RADIUS auth/acct, pppol2tp.so for
# L2TP) and OpenSSL dlopens its legacy provider (MD4/DES for MS-CHAP/MPPE). So we
# take Alpine's musl-built ppp + openssl and copy pppd + those plugins + the
# legacy provider + every shared-lib dependency + the musl loader into a tree
# rooted at a FIXED path (backend.PppdBundleRoot). Instead of patchelf'ing the
# ELF interpreter (which corrupts musl binaries — verified), the tree's entry
# point `sbin/pppd` is a tiny wrapper that invokes the bundled musl loader with
# --library-path, so the whole thing runs on ANY host libc (glibc included).
#
# The tree is tarred to /out/pppd-bundle.tgz, consumed by backend/pppd.go.
# The fixed path MUST match backend/pppd.go's PppdBundleRoot (/usr/libexec/vpn-ui).
set -eu

PREFIX=/usr/libexec/vpn-ui       # must equal backend.PppdBundleRoot
ARCH="${ARCH:-x86_64}"           # musl loader arch (amd64 => x86_64)
LOADER="ld-musl-${ARCH}.so.1"
ROOT=/tmp/pppdroot
DEST="$ROOT$PREFIX"

apk add --no-cache ppp ppp-radius ppp-l2tp openssl >/dev/null

PPP_VER="$(ls /usr/lib/pppd | head -1)"     # e.g. 2.5.0
PLUGSRC="/usr/lib/pppd/$PPP_VER"

mkdir -p "$DEST/sbin" "$DEST/lib" "$DEST/lib/pppd/$PPP_VER" "$DEST/lib/ossl-modules"

# The real pppd binary + the plugins we load (radius.so for RADIUS auth/acct,
# pppol2tp.so for L2TP). Keep the plugin dir laid out exactly like the host so
# pppd's compiled _PATH_PLUGIN (/usr/lib/pppd/<ver>) resolves once /usr/lib/pppd
# is symlinked here.
cp /usr/sbin/pppd "$DEST/sbin/pppd.bin"
for p in radius pppol2tp; do
    cp "$PLUGSRC/$p.so" "$DEST/lib/pppd/$PPP_VER/$p.so"
done

# OpenSSL legacy provider (default provider is built into libcrypto, not a module).
cp /usr/lib/ossl-modules/legacy.so "$DEST/lib/ossl-modules/legacy.so"

# The musl loader (a real file; libc.musl-* is just a symlink to it).
cp "/lib/$LOADER" "$DEST/lib/$LOADER"
ln -sf "$LOADER" "$DEST/lib/libc.musl-${ARCH}.so.1"

# Recursively copy every shared-lib dependency into the bundle's lib/, so nothing
# resolves to a host path.
collect() {
    for f in "$@"; do
        [ -f "$f" ] || continue
        ldd "$f" 2>/dev/null | awk '/=>/ {print $3}' | while read -r lib; do
            [ -f "$lib" ] || continue
            base=$(basename "$lib")
            [ -e "$DEST/lib/$base" ] && continue
            cp -L "$lib" "$DEST/lib/$base"
        done
    done
}
collect "$DEST/sbin/pppd.bin" "$DEST"/lib/pppd/"$PPP_VER"/*.so "$DEST/lib/ossl-modules/legacy.so"
collect "$DEST"/lib/*.so*      # deps-of-deps (libssl -> libcrypto, etc.)

# Entry-point wrapper: run the real pppd through the bundled loader so its ELF
# interpreter and NEEDED libs resolve from the bundle regardless of host libc.
# xl2tpd/pptpd exec /usr/sbin/pppd (symlinked to this) and `exec` preserves the
# passed pppol2tp socket fds + argv.
cat > "$DEST/sbin/pppd" <<EOF
#!/bin/sh
# vpn-ui bundled pppd launcher — do not edit (generated by pppd-bundle.sh).
B=$PREFIX
export OPENSSL_MODULES="\${OPENSSL_MODULES:-\$B/lib/ossl-modules}"
exec "\$B/lib/$LOADER" --library-path "\$B/lib" "\$B/sbin/pppd.bin" "\$@"
EOF
chmod 0755 "$DEST/sbin/pppd"

mkdir -p /out
tar czf /out/pppd-bundle.tgz -C "$ROOT" usr

echo "== pppd-bundle.tgz built (ppp $PPP_VER, wrapper launcher, no patchelf) =="
tar tzf /out/pppd-bundle.tgz | sed "s#^#  #"
ls -lh /out/pppd-bundle.tgz | awk '{print "== size: "$5}'
